package assignments_List;
//WAP on intialization , decalaration and utlization
public class Assignment2_Declare_Intialization 
{
    static void print()
    {
	int a = 20;
	System.out.println(a);
	
	char c = 'S';
	System.out.println(c);
	
	long l = 232423423432l;
	System.out.println(l);
	
	short s = 1232;
	System.out.println(s);
	
	byte b = 100;
	System.out.println(b);
	
	boolean bb = true;
	System.out.println(bb);
	
	float f = 2.12f; // should use f at last
	System.out.println(f);
	
	String s1 = "Shailly";
	System.out.println(s1);
	
	double d = 232837373; //can accept upto 19 digits
	System.out.println(d);		
	
	int age; //another way of intialization
	age = 53;//decalaration
	System.out.println(age);
    }
   
	public static void main(String[] args)
	{
      print();
     
	}

}
